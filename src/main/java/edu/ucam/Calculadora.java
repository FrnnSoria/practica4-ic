package edu.ucam;

/**
 * Clase Calculadora para realizar operaciones basicas.
 */
public final class Calculadora {

    /**
     * Constructor por defecto de la clase.
     */
    public Calculadora() {
    }

    /**
     * Realiza la suma de dos valores enteros.
     * @param a Primer numero entero a sumar.
     * @param b Segundo numero entero a sumar.
     * @return El resultado entero de la suma.
     */
    public int sumar(final int a, final int b) {
        return a + b;
    }
}

